package app;

import java.util.Scanner;

public class App {
    public static void main(String[] args) throws Exception {

        for (int i = 0; i <= 10; i = i + 1){
            System.out.println("The count is " + i);
            System.out.println("Let's keep counting.");
        }


        for (int c = 10; c <= 20; c = c + 1){
            System.out.println("The count is " + c);
        }

        for (int f = 10; f <= 100; f = f + 1){
            System.out.println("The count is " + f);
        }

        for (int b = 1000; b <= 256000; b = b + 1){
            System.out.println("The count is " + b);
        }

        for (int j = 11; j <= 12; j = j + 1){
            System.out.println("The count is " + j);
        }

        for (int p = 10; p >= 0; p = p - 1){
            System.out.println("The count is " + p);
        }



     }
}